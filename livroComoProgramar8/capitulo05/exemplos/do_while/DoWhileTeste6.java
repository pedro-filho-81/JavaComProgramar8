package exemplos.do_while;

public class DoWhileTeste6 {
    public static void main(String[] args) {
        
        // variável
        int count = 1;
        do { // faça
            // imprima
            System.out.printf("%d ", count);
            // incremente 1
            count++;
            // enquanto count for menor ou igual a 10
        } while(count <= 10); // end do while
    } // end main
} //end class

/*
 * Repetição controlado por contador com a instrução for
 */
package exemplos.for_counter;

public class ForCounter2 {
    public static void main(String[] args) {
        // loop for para contar de 1 a 10
        for(int count = 1; count <= 10; count++) {
            // imprime os valores
            System.out.printf("%d ", count);
        } // end for
    } // end main
} // end class
